package org.tpri.sc.view.obt;

public class PartyFeeStatisticsView {
    protected double partyFeeIn;
    protected double partyFeeOut;
    protected int year;
    
    public double getPartyFeeIn() {
        return partyFeeIn;
    }
    public void setPartyFeeIn(double partyFeeIn) {
        this.partyFeeIn = partyFeeIn;
    }
    public double getPartyFeeOut() {
        return partyFeeOut;
    }
    public void setPartyFeeOut(double partyFeeOut) {
        this.partyFeeOut = partyFeeOut;
    }
    public int getYear() {
        return year;
    }
    public void setYear(int year) {
        this.year = year;
    }
   
   
    
}
